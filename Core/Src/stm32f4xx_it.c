/**
  * @brief This function handles System tick timer.
  */
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  HAL_UART_Receive_IT(&huart3, &Received,10);
  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  /* USER CODE BEGIN SysTick_IRQn 1 */
  static int i = 0;
  if (i>500){
	  HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13); // zmieñ stan diody
	  i=0;
  }
  i++;


  // obrót servomechanizmu
 // htim8.Instance->CCR2=100;
  //if(i>35 & i <200)
//	  htim8.Instance->CCR2=i;

  // obrót silnika dolnego
  Position_reference_value[1]-=SPEED_FiltratedValue[1]/7000*2;
  //Position[1]+=SPEED_FiltratedValue[1]/7200*2;
  PID_reference_Value[1] = PWM_P_position_y(Position_reference_value[1],Position[1]);
  if(PID_reference_Value[1]==1 || PID_reference_Value[1]==-1)
	  PID_reference_Value[1]=0;
  PID_reference_Speed[1] = PWM_PID_ramp_y(PID_reference_Value[1])*10;
  PID_reference_Duty[1] = PWM_PID_speed_y(PID_reference_Speed[1],SPEED_getFiltrated_y());
  Speed[1]=PID_reference_Duty[1];


  // obrót silnika górnego
  Position_reference_value[0]-=SPEED_FiltratedValue[0]/8000*2;

  //Position[0]+=SPEED_FiltratedValue[0]/7200*2;
  PID_reference_Value[0] = PWM_P_position(Position_reference_value[0],Position[0]);

  if(PID_reference_Value[0]==1 || PID_reference_Value[0]==-1)
	  PID_reference_Value[0]=0;
  PID_reference_Speed[0] = PWM_PID_ramp(PID_reference_Value[0])*10;
  PID_reference_Duty[0] = PWM_PID_speed(PID_reference_Speed[0],SPEED_getFiltrated_x());
  Speed[0]=PID_reference_Duty[0];
  //Speed[0]=200;


  PWM_Set();
  POSREG_EcoderRead();




  /* USER CODE END SysTick_IRQn 1 */
}

